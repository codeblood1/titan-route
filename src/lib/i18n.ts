export type LanguageCode = (typeof SUPPORTED_LANGUAGES)[number]["code"];
export const RTL_LANGUAGES: LanguageCode[] = ["ar"];

function getBrowserLanguage(): LanguageCode {
  const navLang = navigator.language?.toLowerCase() || "en";
  const supported = SUPPORTED_LANGUAGES.map((l) => l.code);

  // Exact match first
  const exact = supported.find((c) => c === navLang);
  if (exact) return exact;

  // Match base language (e.g., "es-MX" → "es")
  const base = navLang.split("-")[0];
  const baseMatch = supported.find((c) => c === base);
  if (baseMatch) return baseMatch;

  return "en";
}

export function getStoredLanguage(): LanguageCode {
  try {
    const stored = localStorage.getItem("titanroute-lang");
    if (stored) {
      const supported = SUPPORTED_LANGUAGES.map((l) => l.code);
      if (supported.includes(stored as LanguageCode)) return stored as LanguageCode;
    }
  } catch { /* ignore */ }
  return getBrowserLanguage();
}

export function storeLanguage(code: LanguageCode): void {
  try {
    localStorage.setItem("titanroute-lang", code);
  } catch { /* ignore */ }
}

export function isRTL(code: LanguageCode): boolean {
  return RTL_LANGUAGES.includes(code);
}

